
public class cast {

	public static void main(String[] args) {
		int a = 103029770;
		long d = 300;
		double e = 3.1428;
		int f = 'A';
		double g = 3.14;
		
		
		byte bytevalue = (byte) a;
		int intvalue = (int) d;
		char charvalue = (char) f;
		int intvalue2 = (int) g; 
		

		System.out.println(bytevalue);
		System.out.println(intvalue);
		System.out.println(charvalue);
		System.out.println(intvalue2);
	}

}
